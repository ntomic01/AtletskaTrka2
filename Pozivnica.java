public class Pozivnica {

    private Trka trka;

    private Trkac trkac;

    public Pozivnica(Trka trka, Trkac trkac) {
        this.trka = trka;
        this.trkac = trkac;
    }

    public Trka getTrka() {
        return trka;
    }

    public void setTrka(Trka trka) {
        this.trka = trka;
    }

    public Trkac getTrkac() {
        return trkac;
    }

    public void setTrkac(Trkac trkac) {
        this.trkac = trkac;
    }




}
